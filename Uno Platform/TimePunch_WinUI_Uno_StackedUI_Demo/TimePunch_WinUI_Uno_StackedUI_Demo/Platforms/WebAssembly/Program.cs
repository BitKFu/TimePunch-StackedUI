using Uno.UI.Hosting;

namespace TimePunch_WinUI_Uno_StackedUI_Demo;

public class Program
{
    public static async Task Main(string[] args)
    {
        var host = UnoPlatformHostBuilder.Create()
            .App(() => new App())
            .UseWebAssembly()
            .Build();

        await host.RunAsync();
    }
}